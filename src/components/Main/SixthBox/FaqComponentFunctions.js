export const showFaqDropdown1 = () => {
  let x = document.getElementById("faq1");
  let y = document.getElementById("li-1");
  let z = document.getElementById('faqCloseBtn');
  let o = document.getElementById('faqOpenBtn');
  if (x.style.display === "none") {
    x.style.display = "block";
    y.style.margin = "0px 0px 15% 0px";
    z.style.opacity = 1;
    o.style.opacity = 0;
  } else {
    x.style.display = "none";
    y.style.margin = "0px 0px 10px 0px";
    z.style.opacity = 0;
    o.style.opacity = 1;

  }
};
